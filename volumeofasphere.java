// WAP to display/print the volume of a sphere
public class volumeofasphere {
    public static void main(String[] args) {
        int r = 5; // Radius of the sphere
        double v = (4.0 / 3.0) * 3.14 * r * r * r; // Volume of the sphere
        System.out.println(v); // Output will be: 523.3333333333334
    }
}