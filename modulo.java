public class modulo {
    public static void main(String[] args) {
        int a = 10; // Dividend
        int b = 3; // Divisor
        int r = a % b; // Modulo operator to find the remainder
        System.out.println("The remainder when " + a + " is divided by " + b + " is: " + r); // Output will be: The remainder when 10 is divided by 3 is: 1
    }
}